package JavaBasics2;

public class Switch {

	public static void main(String [] args)
	{
		char c='g';
		
		switch(c)
		{
		  case  'a': System.out.println("this is case a");
		  
		  case 'g': System.out.println("this is case g");
		  
		  default: System.out.println("this is default case");
		  
		}
	}
	
	
	
}
